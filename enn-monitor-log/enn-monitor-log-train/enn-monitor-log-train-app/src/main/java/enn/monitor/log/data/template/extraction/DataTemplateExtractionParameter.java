package enn.monitor.log.data.template.extraction;

public class DataTemplateExtractionParameter {
	
	private String index = null;
	private String tfRatio = null;
	private String similarRatio = null;

	public String getIndex() {
		return index;
	}

	public void setIndex(String index) {
		this.index = index;
	}

	public String getTfRatio() {
		return tfRatio;
	}

	public void setTfRatio(String tfRatio) {
		this.tfRatio = tfRatio;
	}

	public String getSimilarRatio() {
		return similarRatio;
	}

	public void setSimilarRatio(String similarRatio) {
		this.similarRatio = similarRatio;
	}

}
